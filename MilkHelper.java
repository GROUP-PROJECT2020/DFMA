package com.example.dfma_app_656995;

public class MilkHelper {

    private String Tag, FarmerName, Buyer, DateToday, Morning, MidDay, Evening, Domestic, Sale;

    public MilkHelper (){}

    //simple break separator


    public String getTag() {
        return Tag;
    }

    public void setTag(String tag) {
        Tag = tag;
    }

    public String getBuyer() {
        return Buyer;
    }

    public void setBuyer(String buyer) {
        Buyer = buyer;
    }

    public String getDateToday() {
        return DateToday;
    }

    public void setDateToday(String dateToday) {
        DateToday = dateToday;
    }

    public String getFarmerName() {
        return FarmerName;
    }

    public void setFarmerName(String farmerName) {
        FarmerName = farmerName;
    }

    public String getMorning() {
        return Morning;
    }

    public void setMorning(String morning) {
        Morning = morning;
    }

    public String getDomestic() {
        return Domestic;
    }

    public void setDomestic(String domestic) {
        Domestic = domestic;
    }

    public String getEvening() {
        return Evening;
    }

    public void setEvening(String evening) {
        Evening = evening;
    }

    public String getMidDay() {
        return MidDay;
    }

    public void setMidDay(String midDay) {
        MidDay = midDay;
    }

    public String getSale() {
        return Sale;
    }

    public void setSale(String sale) {
        Sale = sale;
    }
}
